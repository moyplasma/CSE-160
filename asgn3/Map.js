// Minecraft stuff

function mapInBounds(x, z) {
  return x >= 0 && x < 32 && z >= 0 && z < 32;
}

function addBlock(x, z) {
  if (!mapInBounds(x, z)) return false;
  if (g_map[x][z] >= 4) return false;
  g_map[x][z]++;
  return true;
}

function removeBlock(x, z) {
  if (!mapInBounds(x, z)) return false;
  if (g_map[x][z] <= 0) return false;
  g_map[x][z]--;
  return true;
}
